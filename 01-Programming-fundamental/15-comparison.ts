/* -------------------------------------------------------------------------- */

/* -------------------- // 1. Double Equal | Loose Equal -------------------- */
console.log(1 == 1);
console.log("a" == "a");
console.log("1" == 1);
console.log({} == 1);

/* -------------------- //2. Triple Equal | Strict Equal -------------------- */
console.log("1" === 1);
console.log(Number("1"));

/* ---------------------------- //3. Not Equal to --------------------------- */

console.log(10 != 1);
console.log("1" != 1);
console.log({} != 1);

/* --------------------------- //4. Greather Than --------------------------- */
console.log(10 > 5);
console.log(5 > 5);
console.log(5 >= 5);

/* ----------------------------- //5. Less Than ----------------------------- */
console.log(5 < 10);
console.log(5 < 5);
console.log(5 <= 5);
